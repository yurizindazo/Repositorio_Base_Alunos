temperatura = float(input("Digite a temperatura em Celsius: "))

if temperatura >= 30:
    print("Esta quente!")
elif temperatura >= 20:
    print("Esta agradável.")
elif temperatura >= 10:
    print("Esta frio!")
else:
    print("esta muito frio!")

